function buildFullGrid(): GridTile[] {
  const occupied = new Set(defaultGridTiles.map(t => `${t.row},${t.col}`))
  const maxRow = defaultGridTiles.reduce((max, t) => Math.max(max, t.row), 0)
  const maxCol = defaultGridTiles.reduce((max, t) => Math.max(max, t.col), 0)
  const full: GridTile[] = [...defaultGridTiles]
  for (let row = 0; row <= maxRow; row++) {
    for (let col = 0; col <= maxCol; col++) {
      if (!occupied.has(`${row},${col}`)) {
        full.push({ row, col, type: 'empty' })
      }
    }
  }
  return full
}

export const fullGridTiles: GridTile[] = buildFullGrid()
